import Foundation
import UIKit

extension UIColor {
    class func facebook() -> UIColor {
        return UIColor(red: 0.16, green: 0.40, blue: 0.76, alpha: 1.00)
    }
    class func email() -> UIColor {
        return UIColor(red: 0.18, green: 0.21, blue: 0.26, alpha: 1.00)
    }
    class func view1() -> UIColor {
        return UIColor(red: 0.14, green: 0.15, blue: 0.16, alpha: 1.00)
    }
    class func lane() -> UIColor {
        return UIColor(red: 0.85, green: 0.85, blue: 0.85, alpha: 1.00)
    }
    class func text() -> UIColor {
        return UIColor(red: 1.00, green: 1.00, blue: 1.00, alpha: 1.00)
    }
}
